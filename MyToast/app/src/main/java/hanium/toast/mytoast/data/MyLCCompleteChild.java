package hanium.toast.mytoast.data;

public class MyLCCompleteChild {
    private String mName;
    private String mid;

    public MyLCCompleteChild(String name, String id) {
        mName = name;
        mid = id;
    }

    public String getName() {
        return mName;
    }

    public String getMid() {
        return mid;
    }
}